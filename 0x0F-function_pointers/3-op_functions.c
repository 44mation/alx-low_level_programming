#include "3-calc.h"

/**
 * op_add - addition opperation
 * @a: the first input
 * @b: the seconde input
 * Return: return sum
*/

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subs opperation
 * @a: the first input
 * @b: the seconde input
 * Return: return substruction
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication opperation
 * @a: the first input
 * @b: the seconde input
 * Return: return mul
*/
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - division opperation
 * @a: the first input
 * @b: the seconde input
 * Return: return div
*/
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - modulo opperation
 * @a: the first input
 * @b: the seconde input
 * Return: return mod
*/
int op_mod(int a, int b)
{
	return (a % b);
}
